let fruit = "Mango";
let message = "";

switch (fruit) {
  case "Apple":
    message = "This is an Apple";
    break;

  case "Jackfruit":
    message = "This is an Jackfruit";
    break;

  case "Mango":
    message = "This is a Mango";
    break;

  default:
    message = "No Fruit is Available";
}

console.log(message);
